func (p *prometheusProcessor) setupLogger() {
	prefix := loggingPrefix
	if p.Name != "" {
		prefix += " [" + p.Name + "]"
	}
	p.logger = hclog.New(&hclog.LoggerOptions{
		Name:       prefix,
		Output:     os.Stderr,
		TimeFormat: "2006/01/02 15:04:05.999999",
	})
	if p.Debug {
		p.logger.SetLevel(hclog.Debug)
	}
}

// Get the CiscoWLC struct for a given event.
func (p *prometheusProcessor) getCiscoWLC(event *formatters.EventMsg) (ciscoWLC, error) {
	source, exists := event.Tags["source"]
	if !exists {
		p.logger.Error(
			"source tag missing for event",
			"event",
			event.Name,
			"tags",
			tagsToString(event.Tags),
		)
		return ciscoWLC{}, ErrMissingSourceTag
	}

	wlc, exists := p.wlcs[source]
	if !exists {
		p.logger.Error(
			"source not configured as cisco wlc",
			"event", event.Name,
			"tags", tagsToString(event.Tags),
		)
		return ciscoWLC{}, ErrSourceNotCiscoWLC
	}

	return wlc, nil
}

func main() {
	logger := hclog.New(&hclog.LoggerOptions{
		Output:      os.Stderr,
		DisableTime: true,
	})

	logger.Info(
		"starting plugin processor",
		"name",
		processorType,
		"version",
		version.Version,
		"build_date",
		version.BuildDate,
	)

	plug := &prometheusProcessor{}
	plugin.Serve(&plugin.ServeConfig{
		HandshakeConfig: plugin.HandshakeConfig{
			ProtocolVersion:  1,
			MagicCookieKey:   "GNMIC_PLUGIN",
			MagicCookieValue: "gnmic",
		},
		Plugins: map[string]plugin.Plugin{
			processorType: &event_plugin.EventProcessorPlugin{Impl: plug},
		},
		Logger: logger,
	})

	if plug.eventTraceFile != nil {
		if err := plug.eventTraceFile.Close(); err != nil {
			logger.Error("error closing event trace file")
		}
	}

	logger.Info("reached END", "name", processorType)
}
